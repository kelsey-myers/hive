import mongoose from "mongoose";

const buzzSchema = mongoose.Schema({
  title: String,
  message: String,
  creator: String,
  tags: [String],
  selectedFile: String,
  likeCount: {
    type: Number,
    default: 0,
  },
  createdAt: {
    type: Date,
    default: new Date(),
  },
});

const BuzzMessage = mongoose.model("BuzzMessage", buzzSchema);

export default BuzzMessage;
